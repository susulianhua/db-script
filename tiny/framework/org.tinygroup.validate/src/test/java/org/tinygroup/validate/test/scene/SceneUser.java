package org.tinygroup.validate.test.scene;

public class SceneUser {
    private String name;
    private int age;
    private SceneAddress address;
    private SceneUser mate;
    private int grade;


    public int getGrade() {
        return grade;
    }

    public void setGrade(int grade) {
        this.grade = grade;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public SceneAddress getAddress() {
        return address;
    }

    public void setAddress(SceneAddress address) {
        this.address = address;
    }

    public SceneUser getMate() {
        return mate;
    }

    public void setMate(SceneUser mate) {
        this.mate = mate;
    }


}
